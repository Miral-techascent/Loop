package Loop;

public class For_EO {
    public static void main(String[] args) {
        int i=1;
        System.out.println("Odd Number");
        for (i=1;i<=20;i++){
            System.out.println(i);
            i++;
        }
        System.out.println("Even Number");
        for (i=1;i<=20;i++){
            i++;
            System.out.println(i);
            }


    }
}
